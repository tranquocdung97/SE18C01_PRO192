import java.util.Scanner;
public class Slot2_Exercise22 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        int x = 1;
        while (b > 0)
        {
            x *= a;
            b--;
        }
        System.out.print(x);
    }
}
