
public class Student4 {

    private String name;
    private String address;
    private double gpa;

    public Student4(String name, String address, double gpa) {
        this.name = name;
        this.address = address;
        this.gpa = gpa;
    }

    public String toString() {
        return "Name: " + name + ", address: " + address + ", GPA: " + gpa;
    }
}
