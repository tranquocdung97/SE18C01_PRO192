package packages2;

import java.util.Scanner;

/**
 *
 * @author XUAN HIEN
 */
public class bai53 {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int count = 0;
        for (int i = 1; i <= n; i++) {
            if (n % i == 0) {
                count++;
            }
        }
        System.out.println(count);
    }
}
