package packages2;

import java.util.Scanner;

/**
 *
 * @author XUAN HIEN
 */
public class bai69 {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int m = sc.nextInt();
        int a[][] = new int[n][m];
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < m; j++) {
                a[i][j] = sc.nextInt();
            }
        }
        int sum = 0;
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < m; j++) {
                if (a[i][j] % 5 == 0) {
                    sum += a[i][j];
                }
            }
        }
        System.out.print(sum);
    }
}
