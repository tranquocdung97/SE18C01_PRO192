package packages2;

import java.util.Scanner;

/**
 *
 * @author XUAN HIEN
 */
public class bai61 {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int a[] = new int[n];
        for (int i = 0; i < n; i++) {
            a[i] = sc.nextInt();
        }
        int max = a[0];
        for (int j = 1; j < n; j++) {
            if (a[j] > max) {
                max = a[j];
            }
        }
        System.out.print(max);
    }
}
